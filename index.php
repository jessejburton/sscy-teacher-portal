                <div class="howto">
                    <ol>
                        <li>Click on "CLASSES" in the menu at the top</li>
                        <li>Find the class that you would like to edit and click "<small><i class='fas fa-edit'></i> edit class</small>" underneath the class details</li>
                        <li>Update the title and/or description for the class</li>
                        <li>Click on "Save Class"</li>
                    </ol>

