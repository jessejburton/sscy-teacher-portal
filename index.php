                <div class="howto">
                    <ol>
                        <li>Click on "REGISTRATION" in the menu at the top</li>
                        <li>Select the class and date for the class you would like to record registration for</li>
                        <li>Under registrants you will see a list of anyone who has already been registered or that registered online. Here you can enter the first name, last name and email address for the students you need to register</li>
                        <li>Click on "CHECK IN" to record the registration</li>
                    </ol>

