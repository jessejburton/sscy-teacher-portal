                <div class="howto">
                    <ol>
                        <li>Click on "REGISTRATION" in the menu at the top</li>
                        <li>Select the class and date for the class you would like to record registration for</li>
                        <li>Under Total Amount Collected beside the $ click on "amount collected" and enter the total amount of money that you collected for this class</li>
                        <li>Click on "SAVE" to record the amount <small>* you can change this amount if you enter the wrong value, just remember to hit save again</small></li>
                    </ol>

